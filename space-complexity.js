


/* 
  Space Complexity: O(1);
  Time Complexity: O(1);
*/
function add(num1, num2){
  return num1 + num2;
}


/* 
  Space Complexity: O(n);
  Time Complexity: O(n);
*/
function createArray(num){

  const arr = [];

  for (let i = 0; i < num; i++) {
    arr.push(i);
  }

  return arr;
}

